STEK SEGMENT STACK 'STACK'
		DB	64 DUP (0)
STEK ENDS
	
SDATA SEGMENT 'DATA'
		N	DB	4
		F	DW	?
SDATA ENDS

SCODE SEGMENT
		ASSUME	CS:SCODE,	DS:SDATA,	SS:STEK
	
FACT PROC
		PUSH	BP
		MOV		BP,	SP
NN		EQU		[BP + 4]
		MOV		CX,	NN
		DEC		CX
		JNE		M1
		MOV		AX,	1
		POP		BP
		RET	

M1:
		PUSH	CX
		CALL	FACT
		ADD		SP,	2
		MOV		CX,	NN
		MUL		CX
		POP		BP
		RET
	
FACT ENDP

START PROC
	MOV		AX, SDATA
	MOV		DS, AX
	
	PUSH	N
	CALL	FACT
	MOV		F,	AX
	MOV		AH, 4CH
	INT		21H
START ENDP

SCODE ENDS

END START