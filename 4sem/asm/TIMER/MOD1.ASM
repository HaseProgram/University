SSTACK SEGMENT PARA STACK 'STACK'
	DB 64 DUP('STACK___')
SSTACK ENDS

CSEG SEGMENT PUBLIC 'CODE'
     ASSUME CS:CSEG, SS:SSTACK
	
	EXTRN CLOCKS:NEAR

SHOWTIME: CALL CLOCKS

BUFF:	MOV AX, 0
          
       	INT 16H		;читаем код нажатой клавиши
                                         
	CMP AH, 02H
	JZ SHOWTIME
	CMP AH, 03H
	JZ SHOWTIME
	CMP AH, 04H
	JZ SHOWTIME
	CMP AH, 05H
	JZ SHOWTIME
	CMP AH, 06H
	JZ SHOWTIME
	CMP AH, 07H
	JZ SHOWTIME
	CMP AH, 08H
	JZ SHOWTIME
	CMP AH, 09H
	JZ SHOWTIME
	CMP AH, 0AH
	JZ SHOWTIME
	CMP AH, 0BH
	JZ SHOWTIME

	CMP AH, 01H 	     
        JNZ BUFF	;если не нажато ESC,
         


	MOV AH, 4CH
	MOV AL, 0
	INT 21H


CSEG ENDS
     END BUFF
