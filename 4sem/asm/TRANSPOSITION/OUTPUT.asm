SSTACK SEGMENT PARA STACK 'STACK'
	DB 128 DUP(?)
SSTACK ENDS

DSEG SEGMENT PUBLIC 'DATA'

	EXTRN X:WORD
	N		EQU		4
	
DSEG ENDS

CSEG SEGMENT 'CODE'
	ASSUME CS:CSEG, SS:SSTACK, DS:DSEG

	PUBLIC OUTPUT

OUTPUT PROC

	XOR SI, SI		
	MOV BX, N

COLUMN: MOV CX, N
	
ROW:	
	MOV DL, WORD PTR (X+SI)
	ADD DL, '0'
	MOV AH, 02H
	INT 21H

	MOV AH, 02H
	MOV DL, ' '
	INT 21H

	INC SI
	INC SI

	LOOP ROW

	MOV AH, 2H
	MOV DL, 10
	INT 21H
	MOV DL, 13
	INT 21H
	
	DEC BX
	JNZ COLUMN

	MOV AH, 2H
	MOV DL, 10
	INT 21H
	MOV DL, 10
	INT 21H

	RET

OUTPUT ENDP
CSEG ENDS
     END